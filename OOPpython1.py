#Object-oriented programming temelde 4 ana koldan olusur:
#1) Object(obje): bir obje, kisi, cisim, herhangi bir seyin icerisinde oldugu kapsamdir. # ornek: Firat
#2) Class(sinif): objenin ait oldugu klasman, siniftir. Obje boyle tanimlanir. # ornek: insan
#3) Properties (ozellikler): Objenin sahip oldugu ozelliklerdir, degistirilebilir. # ornek: gitari vardir, bacaklari vardir, arabasi vardir, bilgisayari vardir
#4) Methods (metodlar): Objenin yapabildigi, gordugu islevdir. Neler yapip yapmayacagini burasi belirler. # ornek: gitar calabilir, kosabilir, araba surebilir, kod yazabilir

